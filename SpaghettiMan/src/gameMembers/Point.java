package gameMembers;

/**
 * Represents a point in Cartesian (x,y) space.
 * @author s024000
 *
 */
public class Point {

	/**
	 * The constructor. Sets the x and y variables.
	 * @param x  the x location
	 * @param y  the y location
	 */
	public Point (float x, float y){
		this.x = x;
		this.y = y;
	}
	
	public float x;
	public float y;
}
